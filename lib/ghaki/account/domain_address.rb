############################################################################
require 'ghaki/account/syn_opts'

############################################################################
module Ghaki
  module Account
    module DomainAddress

      ######################################################################
      extend SynOpts 
      attr_syn_opts :domain_address

      ######################################################################
      # OBJECT METHODS
      ######################################################################
      attr_writer :domain_address

      #---------------------------------------------------------------------
      def domain_address
        @domain_address ||= self.make_domain_address
      end

      ######################################################################
      protected
      ######################################################################

      #---------------------------------------------------------------------
      def make_domain_address
        if @userdomain.nil?
          @username
        else
          "\\\\" + @userdomain + "\\" + @username
        end
      end

    end # class
  end # namespace
end # package
